package com.example.khadija.smartfridge.RecipiesDetails;

public class StepsClass {
    int number;
    String step;

    public StepsClass(int number, String step) {
        this.number = number;
        this.step = step;
    }

    public int getNumber() {
        return number;
    }

    public String getStep() {
        return step;
    }
}
